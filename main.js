async function deleteFileAsync(filename) {
  try {
    await unlink(filename);
    console.log('Файл успішно видалено');
  } catch (error) {
    console.error('Помилка при видаленні файлу:', error);
    if (error.code === 'ENOENT') {
      console.error('Файл не існує:', filename);
    }
    return error;
  }
}

